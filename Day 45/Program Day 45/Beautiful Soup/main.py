from bs4 import BeautifulSoup


with open(file="website.html", mode="r", encoding="utf8") as website:
    contents = website.read()

soup = BeautifulSoup(contents, "html.parser")

print(soup)

print("1 ***************************************************************************************************************")

print(soup.prettify())

print("2 ***************************************************************************************************************")

print(soup.title)

print("3 ***************************************************************************************************************")

print(soup.title.name)

print("4 ***************************************************************************************************************")

print(soup.title.string)

print("5 ***************************************************************************************************************")

print(soup.li.string)

print("6 ***************************************************************************************************************")

print(soup.a)

print("7 ***************************************************************************************************************")

print(soup.find_all("li"))

a_tags = soup.find_all("a")

for text in a_tags:
    print(text.getText())

print("8 ***************************************************************************************************************")

print(soup.find(name="h1", id="name").string)

print(soup.find("h3", class_="heading"))

print("9 ***************************************************************************************************************")

print(soup.h3["class"])

print("10 ***************************************************************************************************************")

a_tags = soup.find_all("a")

for text in a_tags:
    print(text.get("href"))

print("11 ***************************************************************************************************************")

print(soup.select_one(selector="#name"))

print("12 ***************************************************************************************************************")

print(soup.select(selector=".heading")[0].getText())

print("13 ***************************************************************************************************************")

print(soup.select(selector="p em"))

